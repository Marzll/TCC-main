

//funções aparecer texto
function apliUm(){
    var texto_1 = document.querySelector(".texto-um");
    if (texto_1.style.fontSize == "0px"){
        texto_1.style.fontSize = "20px";
    } else{
        texto_1.style.fontSize = "0px";
    }
    

}

